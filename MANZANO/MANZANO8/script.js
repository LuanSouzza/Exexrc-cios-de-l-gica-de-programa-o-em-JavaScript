alert("Calculadora do volume de retângulos");

let comprimento = parseInt(prompt("Digite o valor do comprimento do retângulo em cm."));
let largura = parseInt(prompt("Digite o valor da largura do retângulo em cm."));
let altura = parseInt(prompt("Digite o valor da altura do retângulo em cm."));

let volume = comprimento * largura * altura

alert("O valor do volume do retângulo é de:" + volume + "cm³");